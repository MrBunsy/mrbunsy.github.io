//finds x coords of an element on the page
findPosX=function(obj,includeScroll)
  {
    var curleft = 0;
    if(obj.offsetParent)
        while(1) 
        {
          curleft += obj.offsetLeft;
          if(!obj.offsetParent)
            break;
          obj = obj.offsetParent;
        }
    else if(obj.x)
        curleft += obj.x;
		if(includeScroll)
		{
			curleft += document.body.scrollLeft ? document.body.scrollLeft : window.pageXOffset
		}
    return curleft;
  }

