/**
 * Lil static class to give information about the mouse
 */
var mouse = {};
/**
 * The mouse's x coord
 * @var int
 */
mouse.x;
/**
 * The mouse's y coord
 * @var int
 */
mouse.y;
/**
 * Grabs the mouse coords
 */
mouse.grabCoords = function(e) {
	var posx = 0;
	var posy = 0;
	if (!e) var e = window.event;
	if (e.pageX || e.pageY) 	{
		posx = e.pageX;
		posy = e.pageY;
	}
	else if (e.clientX || e.clientY) 	{
		posx = e.clientX + document.body.scrollLeft
			+ document.documentElement.scrollLeft;
		posy = e.clientY + document.body.scrollTop
			+ document.documentElement.scrollTop;
	}
	mouse.x = posx;
	mouse.y = posy;
}
/**
 * Registers mouse.grabCoords to a specified event (eg onclick)
 * @param string the event
 */
mouse.addEvent = function(event) {
	myAddEvent(window,event,mouse.grabCoords);
}

