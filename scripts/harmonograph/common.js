{
    thisSelect=document.getElementById(selectId);
    for(var i=0;i<thisSelect.options.length;i++)
    {
        if(thisSelect.options[i].value==optionValue)
        {
            thisSelect.options[i].selected="selected";
        }
    }
}

//rounds to so many dp
function roundNumber(num, dec)
{
    if(typeof dec != 'number')
    {
        dec=0;
    }
    var result = Math.round(num*Math.pow(10,dec))/Math.pow(10,dec);
    return result;
}

